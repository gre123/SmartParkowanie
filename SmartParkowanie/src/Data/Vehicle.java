package smartparkowanie.simulator;
import java.util.ArrayList;


public class Vehicle {

//******************************************************************************************************************************************//
//	ATRYBUTY																																//
//******************************************************************************************************************************************//
	
	private int id;					// unikalny numer pojazdu
	private String position;		// aktualna pozycja pojazdu- ulica na której siê znajduje(krawêdŸ grafu)
	private String destination;		// pozycja do której zmierza pojazd- wierzcho³ek grafu(parking).
	boolean isParked;				// zmienna okreœlaj¹ca czy pojazd jest w trasie czy mo¿e parkuje
	int waitTime;					// zmienna oznaczaj¹ca czas parkowania/postoju
	
	private ArrayList<DayEvent> dayPlan;
	int dayPlanIndex;

//******************************************************************************************************************************************//
//	KONSTRUKTOR																																//
//******************************************************************************************************************************************//
	
	public Vehicle(int identifier, String pos){
		
		id = identifier;
		isParked = false;
		position = pos;
		destination = null;
		waitTime = 0;
		
		dayPlan = new ArrayList<DayEvent>();
		dayPlanIndex = 0;
	}
	
//******************************************************************************************************************************************//
//	FUNKCJE U¯YTKOWE																														//
//******************************************************************************************************************************************//
	
	public String toString(){
		return "Vehicle: " + id + "\nposition: " + position + "\ndestination: " + destination + "\nisParked: " + isParked + "\nwait Time: " + waitTime + "\n"; 
	}
	
	//--------------------------------------------------------------------------------------------------------------------------------------
	
	private void incrementDayPlanIndex(){
		if(dayPlanIndex < dayPlan.size()){
			dayPlanIndex++;
		}else{
			dayPlanIndex = 0;
		}
	}
	
//******************************************************************************************************************************************//
//	SETTERY																																	//
//******************************************************************************************************************************************//
	
	public void setIsParked(boolean val){
		isParked = val;
	}
	
	//--------------------------------------------------------------------------------------------------------------------------------------
	
	public void setPosition(String pos){
		position = pos;
	}
	
	//--------------------------------------------------------------------------------------------------------------------------------------
	
	public void setWaitTime(int wait){
		waitTime = wait;
	}
	
	//--------------------------------------------------------------------------------------------------------------------------------------
	
	public void setDestination(String dest){
		destination = dest;
	}
	
	//--------------------------------------------------------------------------------------------------------------------------------------
	
	public void setDayPlan(ArrayList<DayEvent> plan){
		dayPlan = plan;
	}

//******************************************************************************************************************************************//
//	SETTERY																																	//
//******************************************************************************************************************************************//

	public boolean getIsParked(){
		return isParked;
	}
	
	//--------------------------------------------------------------------------------------------------------------------------------------
	
	public int getID(){
		return id;
	}
	
	//--------------------------------------------------------------------------------------------------------------------------------------
	
	public int getwaitTime(){
		return waitTime;
	}
	
	//--------------------------------------------------------------------------------------------------------------------------------------
	
	public String getPosition(){
		return position;
	}
	
	//--------------------------------------------------------------------------------------------------------------------------------------
	
	public String getDestination(){
		return destination;
	}
	
	//--------------------------------------------------------------------------------------------------------------------------------------
	
	public ArrayList<DayEvent> getDayPlan(){
		return dayPlan;
	}
	
	//--------------------------------------------------------------------------------------------------------------------------------------
	
	public DayEvent getNextDayEvent(){
		DayEvent event = dayPlan.get(dayPlanIndex);
		incrementDayPlanIndex();
		return event;
	}
}
